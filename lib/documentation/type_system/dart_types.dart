/// The Dart language has special support for the following:
/// 1. Numbers (int, double)
/// 2. Strings (String)
/// 3. Booleans (bool)
/// 4. Lists (List, also known as arrays)
/// 5. Sets (Set)
/// 6. Maps (Map)
/// 7. Runes (Runes; often replaced by the characters API)
/// 8. Symbols (Symbol)
/// 9. The value 'null' (Null)
///
/// Some other types also have special roles in the Dart language::
/// 1. Object - The superclass of all Dart classes except Null.
/// 2. Enum: The superclass of all enums.
/// 3. Future, Stream - Used in asynchrony support.
/// 4. Iterable - Used in for-in loops and in synchronous generator functions.
/// 5. Never - Indicates that an expression can never successfully finish
/// evaluating. Most often used for functions that always throw an exception.
/// 6. dynamic - Indicates that you want to disable static checking.
/// Usually you should use Object or Object? instead.
/// 7. void - Indicates that a value is never used. Often used as a return type.
///
